#!/usr/bin/env node
/**
 * reclaim-historic-urls.mjs
 * ─────────────────────────────────────────────────────────────
 * Reads .ai/migration/migration-ledger.json and generates
 * public/_redirects with 301 rules for Cloudflare Pages.
 *
 * Usage:  node scripts/reclaim-historic-urls.mjs
 *
 * What it does:
 *   1. Parses the migration ledger for legacy → new URL mappings
 *   2. Generates 301 permanent redirects for changed slugs
 *   3. Appends wildcard catches for WP/spam attack paths
 *   4. Writes the result to public/_redirects
 *
 * Cloudflare Pages _redirects spec:
 *   https://developers.cloudflare.com/pages/configuration/redirects/
 * ─────────────────────────────────────────────────────────────
 */

import { readFileSync, writeFileSync, mkdirSync, existsSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const PROJECT_ROOT = resolve(__dirname, '..');

// ── Paths ──────────────────────────────────────────────────
const LEDGER_PATH = resolve(PROJECT_ROOT, '.ai', 'migration', 'migration-ledger.json');
const OUTPUT_DIR = resolve(PROJECT_ROOT, 'public');
const OUTPUT_PATH = resolve(OUTPUT_DIR, '_redirects');

// ── Load migration ledger ──────────────────────────────────
console.log('📖  Reading migration ledger…');
let ledger;
try {
  const raw = readFileSync(LEDGER_PATH, 'utf-8');
  ledger = JSON.parse(raw);
} catch (err) {
  console.error(`❌  Could not read migration ledger at ${LEDGER_PATH}`);
  console.error(`    ${err.message}`);
  process.exit(1);
}

if (!ledger.urls || !Array.isArray(ledger.urls)) {
  console.error('❌  Migration ledger is missing "urls" array.');
  process.exit(1);
}

// ── Build redirect rules ───────────────────────────────────
const lines = [];

// Header
lines.push('# ============================================');
lines.push('# 3 Gen Pressure Washing — Cloudflare Pages Redirects');
lines.push(`# Auto-generated by reclaim-historic-urls.mjs`);
lines.push(`# Generated: ${new Date().toISOString()}`);
lines.push(`# Source: .ai/migration/migration-ledger.json`);
lines.push('# Spec: https://developers.cloudflare.com/pages/configuration/redirects/');
lines.push('# ============================================');
lines.push('');

// ── Section 1: Migration 301s ──────────────────────────────
lines.push('# ─── Legacy URL → New URL (301 Permanent) ───');
lines.push('');

let redirectCount = 0;

for (const entry of ledger.urls) {
  const { legacy_url, new_slug, legacy_title } = entry;

  // Skip entries where the URL hasn't changed
  if (!legacy_url || !new_slug) continue;
  if (legacy_url === new_slug) continue;

  // Normalize: ensure paths don't have trailing slashes for matching
  // but Cloudflare handles both with and without
  const from = legacy_url.endsWith('/') ? legacy_url : legacy_url;
  const to = new_slug.endsWith('/') ? new_slug : new_slug;

  // Add comment with legacy title for traceability
  if (legacy_title) {
    lines.push(`# ${legacy_title}`);
  }
  lines.push(`${from}  ${to}  301`);
  redirectCount++;
}

lines.push('');

// ── Section 2: Trailing slash variants ─────────────────────
// Cloudflare Pages usually handles trailing slashes, but for
// legacy URLs that may have been indexed with trailing slashes
// we add explicit matches for the most important ones.
lines.push('# ─── Trailing Slash Variants ───');
lines.push('');

for (const entry of ledger.urls) {
  const { legacy_url, new_slug } = entry;
  if (!legacy_url || !new_slug) continue;
  if (legacy_url === new_slug) continue;
  if (legacy_url === '/') continue; // homepage doesn't need trailing slash variant

  // Add trailing-slash variant if the legacy URL didn't have one
  if (!legacy_url.endsWith('/')) {
    lines.push(`${legacy_url}/  ${new_slug}  301`);
    redirectCount++;
  }
}

lines.push('');

// ── Section 3: WordPress & CMS Spam Traps ──────────────────
lines.push('# ─── WordPress / CMS / Spam Path Catches ───');
lines.push('# Catches bots probing for WP vulnerabilities and');
lines.push('# prevents 404 pollution in analytics.');
lines.push('');

const spamRules = [
  // WordPress core paths
  ['/wp-admin/*', '/', '301', 'WP admin panel probes'],
  ['/wp-content/*', '/', '301', 'WP content directory probes'],
  ['/wp-includes/*', '/', '301', 'WP includes directory probes'],
  ['/wp-login.php', '/', '301', 'WP login page probes'],
  ['/wp-login.php*', '/', '301', 'WP login page probes with params'],
  ['/wp-cron.php', '/', '301', 'WP cron probes'],
  ['/wp-json/*', '/', '301', 'WP REST API probes'],
  ['/xmlrpc.php', '/', '301', 'WP XML-RPC probes'],
  ['/wp-signup.php', '/', '301', 'WP signup probes'],
  ['/wp-register.php', '/', '301', 'WP register probes'],
  ['/wp-comments-post.php', '/', '301', 'WP comment spam probes'],

  // Common CMS / server probes
  ['/cgi-bin/*', '/', '301', 'CGI-bin probes'],
  ['/admin/*', '/', '301', 'Generic admin probes'],
  ['/administrator/*', '/', '301', 'Joomla admin probes'],
  ['/.env', '/', '301', 'Environment file probes'],
  ['/.git/*', '/', '301', 'Git repository probes'],
  ['/config.php', '/', '301', 'Config file probes'],
  ['/phpmyadmin/*', '/', '301', 'phpMyAdmin probes'],

  // WordPress query string patterns (Cloudflare Pages supports splats)
  // Note: Cloudflare _redirects do NOT support query string matching
  // These catch path-based WP patterns only
  ['/feed', '/', '301', 'WP RSS feed'],
  ['/feed/*', '/', '301', 'WP RSS feed variants'],
  ['/comments/feed/*', '/', '301', 'WP comment feeds'],
  ['/trackback/*', '/', '301', 'WP trackback'],
  ['/wp-sitemap.xml', '/sitemap-index.xml', '301', 'WP sitemap → Astro sitemap'],
  ['/wp-sitemap-*.xml', '/sitemap-index.xml', '301', 'WP sitemap variants'],

  // Old WordPress category/tag/archive paths
  ['/category/*', '/', '301', 'WP category archives'],
  ['/tag/*', '/', '301', 'WP tag archives'],
  ['/author/*', '/', '301', 'WP author archives'],
  ['/archives/*', '/', '301', 'WP date archives'],

  // Common spam/hack attempt paths
  ['/wp-*.php', '/', '301', 'Generic WP PHP file probes'],
  ['/*.asp', '/', '301', 'ASP file probes'],
  ['/*.aspx', '/', '301', 'ASPX file probes'],
  ['/ads.txt', '/', '301', 'Ads.txt probes (no ads)'],
];

for (const [from, to, code, comment] of spamRules) {
  lines.push(`# ${comment}`);
  lines.push(`${from}  ${to}  ${code}`);
}

lines.push('');

// ── Section 4: Legacy Squarespace / Wix / misc CMS paths ───
lines.push('# ─── Misc Legacy CMS Patterns ───');
lines.push('');

const miscRules = [
  // Common Squarespace/Wix patterns that bots try
  ['/s/*', '/', '301', 'Squarespace static asset paths'],
  ['/blog-feed.xml', '/sitemap-index.xml', '301', 'Legacy blog feed'],
  ['/blog/*', '/', '301', 'Legacy blog paths (no blog on new site)'],
  ['/post/*', '/', '301', 'Legacy post paths'],
];

for (const [from, to, code, comment] of miscRules) {
  lines.push(`# ${comment}`);
  lines.push(`${from}  ${to}  ${code}`);
}

// ── Write output ───────────────────────────────────────────
if (!existsSync(OUTPUT_DIR)) {
  mkdirSync(OUTPUT_DIR, { recursive: true });
}

const output = lines.join('\n') + '\n';
writeFileSync(OUTPUT_PATH, output, 'utf-8');

console.log('');
console.log(`✅  Wrote ${OUTPUT_PATH}`);
console.log(`    ${redirectCount} migration redirects`);
console.log(`    ${spamRules.length} spam/WP catch rules`);
console.log(`    ${miscRules.length} misc CMS catch rules`);
console.log(`    ${redirectCount + spamRules.length + miscRules.length} total rules`);
console.log('');
console.log('📋  Next steps:');
console.log('    1. Review public/_redirects');
console.log('    2. Run `astro build` — Cloudflare Pages picks up _redirects automatically');
console.log('    3. Deploy and verify with: curl -I https://3genpressurewashing.com/old-path');
